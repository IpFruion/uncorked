#!/bin/bash

set -e
SOURCE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SOURCE_DIR}/utils.sh"
LUA_OUTPUT="${SOURCE_DIR}/../prototypes/colors.lua"

echo "-- **NOTE** This is an autogenerated file.
-- Do Not modify
SCIENCE_COLOR = {}
" > "$LUA_OUTPUT"

while IFS="|" read -r science r g b a; do
  base=$(basename "$science")
  science_name="${base%.*}"
  echo "Found $science_name adding data"

  echo "SCIENCE_COLOR[\"$science_name\"] = { r = $r, g = $g, b = $b, a = $a }" >> "$LUA_OUTPUT"
done < <(science_packs)
